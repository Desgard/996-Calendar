import UIKit

extension UIColor {
    
    static func showColor() -> UIColor {
        return UIColor(rgb: 0x3d3d3d)
    }
    
    static func highlightColor() -> UIColor {
        return UIColor(rgb: 0x151515)
    }
    
    static func quoteColor() -> UIColor {
        return UIColor(rgb: 0xb5b5b5)
    }
}
